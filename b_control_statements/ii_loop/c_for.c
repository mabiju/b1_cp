// A simple C program to demonstrate the for loop by printing numbers from 0 to 9
// Syntax
// ------
// for(initialization; condition; increment/ decrement){
//     statement to print
// }

#include <stdio.h>
int main()
{
    for (int i = 0; i < 10; i++)
    {
        printf("%d\n", i);
    }
    return 0;
}